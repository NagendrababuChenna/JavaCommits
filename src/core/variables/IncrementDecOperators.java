package core.variables;

public class IncrementDecOperators {

	
	public static void main(String[] args) {
		
		int i =0;
		int j= 0;
		// increment decrement operators 

		
		i +=2;
		System.out.println(i);
		System.out.println(++i);
		
		j = i++ + ++i;
		System.out.println(j);
		
		
	}
}
