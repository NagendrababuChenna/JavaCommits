package core.variables;

/**
 * This class is used to demonstrate the purpose of variables
 * @author nc047b
 *
 */
public class Variables {
	
	
	public static void main(String[] args) {
		
		int a = 10;// Creating integer Variable with name a assigning/initializing value 10
		System.out.println(a);
		// Once declared we can use this variable in this class as many time 
		a = 20;
		a = 30;
		a = 40;
		// Changing the values to variable.. always last value overwrite previous one
		System.out.println(a);
		
		float b = 10.5f;
		
		boolean c = true;//(true / false)
		c = false;
		System.out.println(c);
		System.out.println(b);
			
		
	}
	
}
